// The Row/Rows pair one nested array compiles to, mirroring the top-level
// array section's row idiom: the Row binds template paths (two numeric
// holes when the parent is itself an array row), the Rows component runs
// the bound field-array hook and owns add/remove.
const nestedArrayComponents = (
  ui: ModuleUi,
  naming: Naming,
  entry: NestedArrayEntry,
): Readonly<{ lines: readonly string[]; imports: readonly ImportLine[] }> => {
  const rowName = `${entry.stem}Row`;
  const rowsName = `${entry.stem}Rows`;
  const emptyName = `empty${entry.stem}Item`;
  const shell = arrayShell(ui, entry.label);
  const wrap = nestedListShell(ui, entry.label);
  const scalarItem = isScalar(entry.item);

  const itemLeaves =
    entry.item.kind === "object"
      ? entry.item.fields.filter(
          (field) => !isUnaddressable(field.name) && isScalar(field.spec),
        )
      : [];
  // Only one extraction level is generated per array: anything non-scalar
  // inside THIS row's item stays a TODO comment.
  const itemTodos =
    entry.item.kind === "object"
      ? entry.item.fields.flatMap((field) => {
          if (isUnaddressable(field.name)) {
            return [
              `      {/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
            ];
          }
          return isScalar(field.spec)
            ? []
            : [
                `      {/* TODO: nested ${field.spec.kind} ${commentText(q(`${entry.commentBase}.$\{index}.${field.name}`))} — one extraction level is generated per array; extract the next level by hand */}`,
              ];
        })
      : [];

  const used = new Set<string>([
    ...(entry.parented ? ["parentIndex"] : []),
    "index",
    "onRemove",
    "field",
    "rows",
  ]);
  const rowVars = itemLeaves.map((field) => {
    // camelIdent, not camelCase: these become const bindings, and a field
    // named "new"/"delete" would otherwise emit a reserved-word declaration.
    const base =
      camelIdent(field.name).length === 0 ? "value" : camelIdent(field.name);
    const name = `${base}${identifierSuffix(base, used)}`;
    used.add(name);
    return { field, varName: name };
  });

  const rowSpecs =
    entry.item.kind === "object"
      ? itemLeaves.map((f) => f.spec)
      : scalarItem
        ? [entry.item]
        : [];

  const dynamicId = (name: string | undefined): string =>
    name === undefined
      ? `{\`${entry.templateBase}.\${index}\`}`
      : `{\`${entry.templateBase}.\${index}.${templateEscape(name)}\`}`;

  const rowBindings =
    entry.item.kind === "object"
      ? rowVars.map(
          ({ field, varName }) =>
            `  const ${varName} = ${naming.hook("Field")}(\`${entry.templateBase}.\${index}.${templateEscape(field.name)}\`);`,
        )
      : scalarItem
        ? [
            `  const field = ${naming.hook("Field")}(\`${entry.templateBase}.\${index}\`);`,
          ]
        : [];

  const rowBody =
    entry.item.kind === "object"
      ? [
          ...rowVars.flatMap(({ field, varName }) =>
            leafControl(
              ui,
              field.spec,
              varName,
              jsxText(field.label),
              dynamicId(field.name),
              "      ",
            ),
          ),
          ...itemTodos,
        ]
      : scalarItem
        ? leafControl(
            ui,
            entry.item,
            "field",
            jsxText(entry.label),
            dynamicId(undefined),
            "      ",
          )
        : [
            // An array-of-arrays item: the inner array is already one level
            // deeper than this extraction.
            `      {/* TODO: nested array ${commentText(q(`${entry.commentBase}.$\{index}`))} — one extraction level is generated per array; extract the next level by hand */}`,
          ];

  const lines = [
    blankNeedsCast(entry.item)
      ? `const ${emptyName} = ${emitInitialValues(entry.item, 0)} as unknown as ${entry.itemTypeExpr};`
      : `const ${emptyName}: ${entry.itemTypeExpr} = ${emitInitialValues(entry.item, 0)};`,
    "",
    `const ${rowName} = ({`,
    ...(entry.parented ? ["  parentIndex,"] : []),
    "  index,",
    "  onRemove,",
    entry.parented
      ? "}: Readonly<{ parentIndex: number; index: number; onRemove: () => void }>) => {"
      : "}: Readonly<{ index: number; onRemove: () => void }>) => {",
    ...rowBindings,
    "  return (",
    ...shell.rowOpen,
    ...rowBody,
    ...shell.rowClose("onRemove"),
    "  );",
    "};",
    "",
    ...(entry.parented
      ? [
          `const ${rowsName} = ({`,
          "  parentIndex,",
          "}: Readonly<{ parentIndex: number }>) => {",
        ]
      : [`const ${rowsName} = () => {`]),
    `  const rows = ${naming.hook("FieldArray")}(${entry.listArg});`,
    "  return (",
    ...wrap.open,
    "      {rows.fields.map((row, index) => (",
    `        <${rowName}`,
    "          key={row.id}",
    ...(entry.parented ? ["          parentIndex={parentIndex}"] : []),
    "          index={index}",
    "          onRemove={() => rows.remove(index)}",
    "        />",
    "      ))}",
    ...shell.listError,
    ...shell.addButton(
      `() => rows.push(${emptyName})`,
      `Add ${entry.label.toLowerCase()}`,
    ),
    ...wrap.close,
    "  );",
    "};",
  ];

  return {
    lines,
    imports: [
      ...rowSpecs.flatMap((s) => leafImports(ui, s)),
      ...shell.imports,
      ...wrap.imports,
    ],
  };
};

const objectSectionFile = (
  ui: ModuleUi,
  visual: VisualOptions,
  naming: Naming,
  section: SectionPlan,
  plan: Plan,
): ModuleFile => {
  const spec = section.spec as ObjectSpec;
  const propsType = `${section.componentName}Props`;
  const hookName = `use${section.componentName}`;
  const own = (segments: readonly string[]): FieldPlan | undefined =>
    plan.fields.find((f) => f.segments.join(".") === segments.join("."));

  // Arrays anywhere under this section each get a Row/Rows pair in this
  // file; the body renders <XRows /> at the array's site.
  const nested = objectNestedEntries(
    naming,
    collectNestedArrays(spec.fields, [section.key]),
  );
  const nestedByKey = new Map(nested.map((entry) => [entry.key, entry]));
  const nestedParts = nested.map((entry) =>
    nestedArrayComponents(ui, naming, entry),
  );

  const body = (
    fields: readonly NamedField[],
    segments: readonly string[],
    indent: string,
  ): readonly string[] =>
    fields.flatMap((field): readonly string[] => {
      if (isUnaddressable(field.name)) {
        return [
          `${indent}{/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
        ];
      }
      const at = [...segments, field.name];
      switch (field.spec.kind) {
        case "object":
          return [
            `${indent}<fieldset${
              visual.columns > 1
                ? ui === "shadcn"
                  ? ` className="md:col-span-full"`
                  : ` style={{ gridColumn: "1 / -1" }}`
                : ""
            }>`,
            `${indent}  <legend>${jsxText(field.label)}</legend>`,
            ...body(field.spec.fields, at, `${indent}  `),
            `${indent}</fieldset>`,
          ];
        case "array": {
          const entry = nestedByKey.get(at.join("."));
          return entry === undefined
            ? [
                `${indent}{/* TODO: nested array ${commentText(q(at.join(".")))} — extract a row component with its own ${naming.hook("FieldArray")} */}`,
              ]
            : [`${indent}<${entry.stem}Rows />`];
        }
        case "union":
          // A union nested inside an object section: only top-level unions
          // get a generated section; deeper ones are left to extract by hand.
          return [
            `${indent}{/* TODO: discriminated union ${commentText(q(at.join(".")))} — only top-level unions are generated; bind the discriminant with ${naming.hook("Field")} and its variant fields with ${naming.hook("VariantField")} */}`,
          ];
        default: {
          const planned = own(at);
          return planned === undefined
            ? []
            : [`${indent}<${planned.componentName} />`];
        }
      }
    });

  const fieldImports = collectLeaves(spec.fields, [section.key])
    .map(({ segments }) => own(segments))
    .flatMap((planned) => (planned === undefined ? [] : [planned]))
    .map(
      (planned) =>
        `import { ${planned.componentName} } from "../fields/${planned.componentName}";`,
    );

  // Nested rows bind template paths and push typed empty items, so the file
  // gains the bound Field/FieldArray hooks and the draft values type.
  const hooksImports =
    nested.length === 0
      ? [
          `import { ${naming.hook("IsDirty")}, ${naming.hook("IsValid")} } from "../hooks";`,
        ]
      : [
          "import {",
          `  ${naming.hook("Field")},`,
          `  ${naming.hook("FieldArray")},`,
          `  ${naming.hook("IsDirty")},`,
          `  ${naming.hook("IsValid")},`,
          `} from "../hooks";`,
          `import type { ${naming.valuesType} } from "../types";`,
        ];

  return {
    path: `sections/${section.componentName}.tsx`,
    content: [
      HEADER,
      ...mergeImports([
        ...objectSectionImports(ui, visual),
        ...nestedParts.flatMap((part) => part.imports),
      ]),
      ...hooksImports,
      ...fieldImports,
      "",
      `export type ${propsType} = Readonly<{ heading?: string }>;`,
      "",
      "// The section hook is the path-scoped flags: dirty/valid for this",
      "// subtree only, as boolean-only subscriptions.",
      `export const ${hookName} = () => ({`,
      `  dirty: ${naming.hook("IsDirty")}(${q(section.key)}),`,
      `  valid: ${naming.hook("IsValid")}(${q(section.key)}),`,
      "});",
      "",
      ...nestedParts.flatMap((part) => [...part.lines, ""]),
      `export const ${section.componentName} = ({`,
      `  heading = ${q(section.label)},`,
      `}: ${propsType}) => {`,
      `  const { dirty, valid } = ${hookName}();`,
      "  return (",
      ...objectShell(ui, visual, body(spec.fields, [section.key], "      ")),
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const arraySectionFile = (
  ui: ModuleUi,
  visual: VisualOptions,
  naming: Naming,
  section: SectionPlan,
): ModuleFile => {
  const spec = section.spec as Extract<FieldSpec, Readonly<{ kind: "array" }>>;
  const propsType = `${section.componentName}Props`;
  const hookName = `use${section.componentName}`;
  const sectionStem = section.componentName.replace(/Section$/, "");
  const rowName = `${sectionStem}Row`;
  const emptyName = `empty${sectionStem}Item`;
  const shell = arrayShell(ui, section.label);
  const itemLeaves =
    spec.item.kind === "object"
      ? spec.item.fields.filter(
          (field) => !isUnaddressable(field.name) && isScalar(field.spec),
        )
      : [];

  // Arrays directly inside this array's row items each get their own
  // Row/Rows pair, threaded the parent row's index (two numeric holes in
  // every bound path). Nested objects keep their TODO.
  const nested = arrayNestedEntries(
    naming,
    section.key,
    sectionStem,
    spec.item.kind === "object" ? spec.item.fields : [],
  );
  const nestedByKey = new Map(nested.map((entry) => [entry.key, entry]));
  const nestedParts = nested.map((entry) =>
    nestedArrayComponents(ui, naming, entry),
  );

  const itemExtras =
    spec.item.kind === "object"
      ? spec.item.fields.flatMap((field) => {
          if (isUnaddressable(field.name)) {
            return [
              `      {/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
            ];
          }
          if (field.spec.kind === "array") {
            const entry = nestedByKey.get(field.name);
            return entry === undefined
              ? []
              : [`      <${entry.stem}Rows parentIndex={index} />`];
          }
          return isScalar(field.spec)
            ? []
            : [
                `      {/* TODO: nested ${field.spec.kind} ${commentText(q(`${section.key}.$\{index}.${field.name}`))} — extract a row component with its own hook */}`,
              ];
        })
      : [];

  const used = new Set<string>(["index", "onRemove", "field", "rows"]);
  const rowVars = itemLeaves.map((field) => {
    // camelIdent, not camelCase: these become const bindings, and a field
    // named "new"/"delete" would otherwise emit a reserved-word declaration.
    const base =
      camelIdent(field.name).length === 0 ? "value" : camelIdent(field.name);
    const name = `${base}${identifierSuffix(base, used)}`;
    used.add(name);
    return { field, varName: name };
  });

  // A discriminated-union item can't be bound at a dynamic array-row path
  // (useVariantField needs a static union path), so it emits no leaf control,
  // no scalar row binding, and no union-only builder import — just a TODO,
  // mirroring the single-file backend's unreachable-container handling.
  const scalarItem = isScalar(spec.item);

  const rowSpecs =
    spec.item.kind === "object"
      ? itemLeaves.map((f) => f.spec)
      : scalarItem
        ? [spec.item]
        : [];

  const dynamicId = (name: string | undefined): string =>
    name === undefined
      ? `{\`${templateEscape(section.key)}.\${index}\`}`
      : `{\`${templateEscape(section.key)}.\${index}.${templateEscape(name)}\`}`;

  const rowBindings =
    spec.item.kind === "object"
      ? rowVars.map(
          ({ field, varName }) =>
            `  const ${varName} = ${naming.hook("Field")}(\`${templateEscape(section.key)}.\${index}.${templateEscape(field.name)}\`);`,
        )
      : scalarItem
        ? [
            `  const field = ${naming.hook("Field")}(\`${templateEscape(section.key)}.\${index}\`);`,
          ]
        : [];

  const rowBody =
    spec.item.kind === "object"
      ? [
          ...rowVars.flatMap(({ field, varName }) =>
            leafControl(
              ui,
              field.spec,
              varName,
              jsxText(field.label),
              dynamicId(field.name),
              "      ",
            ),
          ),
          ...itemExtras,
        ]
      : scalarItem
        ? leafControl(
            ui,
            spec.item,
            "field",
            jsxText(section.label),
            dynamicId(undefined),
            "      ",
          )
        : [
            `      {/* TODO: array item is a discriminated union — bind its fields by hand with ${naming.hook("VariantField")} (dynamic array-row paths aren't generated) */}`,
          ];

  const imports = mergeImports([
    ...rowSpecs.flatMap((s) => leafImports(ui, s)),
    ...shell.imports,
    // The outer wrapper is objectShell, so panel/collapsible chrome pulls
    // its own components in.
    ...objectSectionImports(ui, visual),
    ...nestedParts.flatMap((part) => part.imports),
  ]);

  // useXField is only imported when a row actually binds a field: an
  // object/scalar item binds one, but a discriminated-union item binds
  // nothing (its row is a TODO), so importing it would be an unused import
  // that breaks a consumer's noUnusedLocals.
  const usesField = [
    ...rowBindings,
    ...nestedParts.flatMap((part) => part.lines),
  ].some((line) => line.includes(`${naming.hook("Field")}(`));

  return {
    path: `sections/${section.componentName}.tsx`,
    content: [
      HEADER,
      ...imports,
      "import {",
      ...(usesField ? [`  ${naming.hook("Field")},`] : []),
      `  ${naming.hook("FieldArray")},`,
      `  ${naming.hook("IsDirty")},`,
      `  ${naming.hook("IsValid")},`,
      `} from "../hooks";`,
      `import type { ${naming.valuesType} } from "../types";`,
      "",
      `export type ${propsType} = Readonly<{ heading?: string }>;`,
      "",
      "// The array section's hook carries the field array alongside the",
      "// path-scoped flags; row fields bind template paths, so they stay",
      "// schema-typed.",
      `export const ${hookName} = () => ({`,
      `  rows: ${naming.hook("FieldArray")}(${q(section.key)}),`,
      `  dirty: ${naming.hook("IsDirty")}(${q(section.key)}),`,
      `  valid: ${naming.hook("IsValid")}(${q(section.key)}),`,
      "});",
      "",
      blankNeedsCast(spec.item)
        ? `const ${emptyName} = ${emitInitialValues(spec.item, 0)} as unknown as NonNullable<${naming.valuesType}[${q(section.key)}]>[number];`
        : `const ${emptyName}: NonNullable<${naming.valuesType}[${q(section.key)}]>[number] = ${emitInitialValues(spec.item, 0)};`,
      "",
      ...nestedParts.flatMap((part) => [...part.lines, ""]),
      `const ${rowName} = ({`,
      "  index,",
      "  onRemove,",
      "}: Readonly<{ index: number; onRemove: () => void }>) => {",
      ...rowBindings,
      "  return (",
      ...shell.rowOpen,
      ...rowBody,
      ...shell.rowClose("onRemove"),
      "  );",
      "};",
      "",
      `export const ${section.componentName} = ({`,
      `  heading = ${q(section.label)},`,
      `}: ${propsType}) => {`,
      `  const { rows, dirty, valid } = ${hookName}();`,
      "  return (",
      ...objectShell(ui, visual, [
        "      {rows.fields.map((row, index) => (",
        `        <${rowName}`,
        "          key={row.id}",
        "          index={index}",
        "          onRemove={() => rows.remove(index)}",
        "        />",
        "      ))}",
        ...shell.listError,
        ...shell.addButton(
          `() => rows.push(${emptyName})`,
          `Add ${section.label.toLowerCase()}`,
        ),
      ]),
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const formFile = (ui: ModuleUi, naming: Naming, plan: Plan): ModuleFile => {
  const shell = formShell(ui);
  return {
    path: `${naming.formName}.tsx`,
    content: [
      HEADER,
      ...mergeImports(shell.imports),
      `import { ${naming.formConst}, ${naming.hook("IsSubmitting")} } from "./hooks";`,
      ...plan.rootFields.map(
        (field) =>
          `import { ${field.componentName} } from "./fields/${field.componentName}";`,
      ),
      ...plan.sections.map(
        (section) =>
          `import { ${section.componentName} } from "./sections/${section.componentName}";`,
      ),
      "",
      "// The form body composes sections; sections compose fields; fields bind",
      "// paths. Nothing in this tree passes `form` — every hook comes pre-wired",
      "// from ./hooks.",
      `export const ${naming.formName} = () => {`,
      `  const submitting = ${naming.hook("IsSubmitting")}();`,
      "  return (",
      ...shell.open(naming),
      ...plan.rootTodos.map((todo) => `${shell.bodyIndent}${todo}`),
      ...plan.rootFields.map(
        (field) => `${shell.bodyIndent}<${field.componentName} />`,
      ),
      ...plan.sections.map(
        (section) => `${shell.bodyIndent}<${section.componentName} />`,
      ),
      ...shell.close,
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const indexFile = (naming: Naming): ModuleFile => ({
  path: "index.ts",
  content: [
    HEADER,
    `export { ${naming.formName} } from "./${naming.formName}";`,
    "// The module's public API: the pre-wired hooks, plus the schema and",
    "// draft types consumers need to type their own submit handlers and",
    "// server code.",
    `export * from "./hooks";`,
    `export * from "./schema";`,
    `export * from "./types";`,
    "",
  ].join("\n"),
});

// A top-level discriminated union → its own section file: the discriminant
// binds with the bound useXField (a common key), each variant-only field
// with useXVariantField (deduped by name), and the JSX switches variant
// blocks on the discriminant's live value. Nested unions stay TODO comments.
type UnionSectionBinding = Readonly<{
  name: string;
  label: string;
  spec: FieldSpec;
  varName: string;
}>;

const unionSectionFile = (
  ui: ModuleUi,
  visual: VisualOptions,
  naming: Naming,
  section: SectionPlan,
): ModuleFile => {
  const spec = section.spec as Extract<FieldSpec, Readonly<{ kind: "union" }>>;
  const propsType = `${section.componentName}Props`;
  const hookName = `use${section.componentName}`;
  const path = section.key;
  const discriminantVar = camelIdent(spec.discriminant);
  const discriminantPath = `${path}.${spec.discriminant}`;
  const commonNames = unionCommonFieldNames(spec);

  // Scalar variant fields, deduped by name, split into common keys (bound with
  // the plain useXField on `path.name`, since useXVariantField rejects them)
  // and variant-only keys (useXVariantField).
  const partitioned = spec.variants
    .flatMap((variant) => variant.fields)
    .filter((field) => !isUnaddressable(field.name) && isScalar(field.spec))
    .reduce<
      Readonly<{
        used: ReadonlySet<string>;
        seen: ReadonlySet<string>;
        common: readonly UnionSectionBinding[];
        variant: readonly UnionSectionBinding[];
      }>
    >(
      (acc, field) => {
        if (acc.seen.has(field.name)) return acc;
        const base =
          camelIdent(field.name).length === 0 ? "value" : camelIdent(field.name);
        const varName = `${base}${identifierSuffix(base, acc.used)}`;
        const binding = {
          name: field.name,
          label: field.label,
          spec: field.spec,
          varName,
        };
        const isCommon = commonNames.has(field.name);
        return {
          used: new Set([...acc.used, varName]),
          seen: new Set([...acc.seen, field.name]),
          common: isCommon ? [...acc.common, binding] : acc.common,
          variant: isCommon ? acc.variant : [...acc.variant, binding],
        };
      },
      {
        used: new Set<string>([discriminantVar]),
        seen: new Set<string>(),
        common: [],
        variant: [],
      },
    );
  const commonBindings = partitioned.common;
  const bindings = partitioned.variant;
  const commonBindingNames = new Set(commonBindings.map((b) => b.name));
  const bindingByName = new Map(bindings.map((binding) => [binding.name, binding]));

  const discriminantSpec: FieldSpec = {
    kind: "enum",
    options: spec.variants.map((variant) => variant.tag),
    optional: false,
    nullable: false,
  };

  const children = [
    ...leafControl(
      ui,
      discriminantSpec,
      discriminantVar,
      jsxText(section.label),
      `{${discriminantVar}.path}`,
      "      ",
    ),
    // Common fields exist in every variant → render once, outside the blocks.
    ...commonBindings.flatMap((binding) =>
      leafControl(
        ui,
        binding.spec,
        binding.varName,
        jsxText(binding.label),
        `{${binding.varName}.path}`,
        "      ",
      ),
    ),
    ...spec.variants.flatMap((variant) => [
      `      {${discriminantVar}.value === ${q(variant.tag)} && (`,
      "        <>",
      ...variant.fields.flatMap((field): readonly string[] => {
        if (isUnaddressable(field.name)) {
          return [
            `          {/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
          ];
        }
        // Common fields already rendered above; only variant-only fields here.
        if (commonBindingNames.has(field.name)) return [];
        const binding = bindingByName.get(field.name);
        return binding === undefined
          ? [
              `          {/* TODO: nested ${field.spec.kind} ${commentText(q(`${path}.${field.name}`))} inside a union variant — extract it by hand */}`,
            ]
          : leafControl(
              ui,
              field.spec,
              binding.varName,
              jsxText(field.label),
              `{${binding.varName}.path}`,
              "          ",
            );
      }),
      "        </>",
      "      )}",
    ]),
  ];

  const leafSpecs = [
    discriminantSpec,
    ...commonBindings.map((binding) => binding.spec),
    ...bindings.map((binding) => binding.spec),
  ];
  const imports = mergeImports([
    ...objectSectionImports(ui, visual),
    ...leafSpecs.flatMap((leaf) => leafImports(ui, leaf)),
  ]);

  const hookImports = [
    naming.hook("Field"),
    ...(bindings.length > 0 ? [naming.hook("VariantField")] : []),
    naming.hook("IsDirty"),
    naming.hook("IsValid"),
  ];

  return {
    path: `sections/${section.componentName}.tsx`,
    content: [
      HEADER,
      ...imports,
      "import {",
      ...hookImports.map((name) => `  ${name},`),
      `} from "../hooks";`,
      "",
      `export type ${propsType} = Readonly<{ heading?: string }>;`,
      "",
      "// The section hook is the path-scoped flags: dirty/valid for this",
      "// union subtree only, as boolean-only subscriptions.",
      `export const ${hookName} = () => ({`,
      `  dirty: ${naming.hook("IsDirty")}(${q(path)}),`,
      `  valid: ${naming.hook("IsValid")}(${q(path)}),`,
      "});",
      "",
      `export const ${section.componentName} = ({`,
      `  heading = ${q(section.label)},`,
      `}: ${propsType}) => {`,
      `  const { dirty, valid } = ${hookName}();`,
      "  // The discriminant and common fields bind with the plain field hook",
      "  // (common keys); the discriminant's live value chooses which variant",
      "  // block renders. Every variant field hook is called unconditionally",
      "  // (React's rules).",
      `  const ${discriminantVar} = ${naming.hook("Field")}(${q(discriminantPath)});`,
      ...commonBindings.map(
        (binding) =>
          `  const ${binding.varName} = ${naming.hook("Field")}(${q(`${path}.${binding.name}`)});`,
      ),
      ...bindings.map(
        (binding) =>
          `  const ${binding.varName} = ${naming.hook("VariantField")}(${q(path)}, ${q(binding.name)});`,
      ),
      "  return (",
      ...objectShell(ui, visual, children),
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

