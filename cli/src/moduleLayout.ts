const schemaFile = (
  schemaImport: SchemaImport,
  schemaSource: string | undefined,
): ModuleFile => ({
  path: "schema.ts",
  content:
    schemaSource !== undefined
      ? `${HEADER}\n${schemaSource}`
      : [
          HEADER,
          "// The schema's source of truth stays where it was; this re-export",
          "// gives every file in the module one import path for it.",
          schemaImport.kind === "default"
            ? `export { default as ${schemaImport.name} } from ${q(schemaImport.from)};`
            : `export { ${schemaImport.name} } from ${q(schemaImport.from)};`,
          "",
        ].join("\n"),
});

const typesFile = (naming: Naming, schemaImport: SchemaImport): ModuleFile => ({
  path: "types.ts",
  content: [
    HEADER,
    `import type { z } from "zod";`,
    `import { ${schemaImport.name} } from "./schema";`,
    "",
    `export type ${naming.schemaType} = typeof ${schemaImport.name};`,
    "",
    "// The draft shape the hooks read and write (z.input: what the form",
    "// holds while being filled, before validation refines it).",
    `export type ${naming.valuesType} = z.input<${naming.schemaType}>;`,
    "",
  ].join("\n"),
});

const hooksFile = (
  naming: Naming,
  schemaImport: SchemaImport,
  root: ObjectSpec,
  plan: Plan,
): ModuleFile => {
  const hasArrays = plan.sections.some((s) => s.spec.kind === "array");
  const hasFields = plan.fields.length > 0 || hasArrays;
  const hooks = [
    ...(hasFields ? [naming.hook("Field")] : []),
    ...(hasArrays ? [naming.hook("FieldArray")] : []),
    ...(plan.sections.length > 0
      ? [naming.hook("IsDirty"), naming.hook("IsValid")]
      : []),
    naming.hook("IsSubmitting"),
  ];
  return {
    path: "hooks.ts",
    content: [
      HEADER,
      `import { createForm, createFormHooks } from "formstand";`,
      `import { ${schemaImport.name} } from "./schema";`,
      `import type { ${naming.valuesType} } from "./types";`,
      "",
      "// A form starts blank: required numbers/dates/enums begin undefined, so",
      "// these initial values intentionally do not satisfy the schema yet —",
      "// hence the cast. Validation reports the gaps on submit.",
      `const initialValues = ${emitInitialValues(root, 0)} as unknown as ${naming.valuesType};`,
      "",
      "// One module-level form for the whole feature; every file below imports",
      "// these pre-wired hooks instead of receiving `form` via props or a",
      "// provider. A module singleton never unmounts — for a per-mount",
      "// lifecycle, use useForm + createFormContext instead.",
      `export const ${naming.formConst} = createForm(${schemaImport.name}, {`,
      "  initialValues,",
      `  mode: "onBlur",`,
      "});",
      "",
      "export const {",
      ...hooks.map((name) => `  ${name},`),
      `} = createFormHooks(${naming.formConst}, ${q(naming.factoryArg)});`,
      "",
    ].join("\n"),
  };
};

// The kit adapter the single-file backends inline, exported once — only for
// the kit uis, and only when some leaf actually renders a control.
const adapterFile = (ui: ModuleUi, usage: KindUsage): ModuleFile | undefined => {
  const hasLeaf =
    usage.string || usage.date || usage.number || usage.boolean || usage.enum;
  if (ui === "plain" || !hasLeaf) return undefined;
  const needsText = usage.string || usage.date;
  const needsChangeEvent = needsText || usage.number || (ui === "mui" && usage.enum);
  const formstandValues = [
    ...(usage.number ? ["numberToInputText", "parseNumberText"] : []),
  ];
  return {
    path: ui === "mui" ? "adapter.ts" : "adapter.tsx",
    content: [
      HEADER,
      ...(needsChangeEvent ? [`import type { ChangeEvent } from "react";`] : []),
      "import {",
      ...formstandValues.map((name) => `  ${name},`),
      "  type UseFieldReturn,",
      `} from "formstand";`,
      "",
      ui === "mui"
        ? muiAdapterSection(usage, "export ")
        : shadcnAdapterSection(usage, "export "),
      "",
    ].join("\n"),
  };
};

const fieldFile = (
  ui: ModuleUi,
  naming: Naming,
  field: FieldPlan,
): ModuleFile => {
  const path = field.segments.join(".");
  const propsType = `${field.componentName}Props`;
  const hookName = `use${field.componentName}`;
  const dateCast = isDateBinding(field.spec);
  const imports = mergeImports([
    ...leafImports(ui, field.spec),
    ...(dateCast ? [{ from: "formstand", names: ["type UseFieldReturn"] }] : []),
  ]);
  return {
    path: `fields/${field.componentName}.tsx`,
    content: [
      HEADER,
      ...imports,
      `import { ${naming.hook("Field")} } from "../hooks";`,
      "",
      `export type ${propsType} = Readonly<{ label?: string }>;`,
      "",
      `export const ${hookName} = () => ${naming.hook("Field")}(${q(path)});`,
      "",
      `export const ${field.componentName} = ({`,
      `  label = ${q(field.label)},`,
      `}: ${propsType}) => {`,
      ...todoTexts(field.spec).map((text) => `  // TODO: ${text}`),
      `  const field = ${hookName}()${dateCast ? DATE_CAST : ""};`,
      "  return (",
      ...leafJsx(ui, field.spec, "field", "{label}", `{${q(path)}}`, "    "),
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const objectSectionFile = (
  ui: ModuleUi,
  visual: VisualOptions,
  naming: Naming,
  section: SectionPlan,
  plan: Plan,
): ModuleFile => {
  const spec = section.spec as ObjectSpec;
  const propsType = `${section.componentName}Props`;
  const hookName = `use${section.componentName}`;
  const own = (segments: readonly string[]): FieldPlan | undefined =>
    plan.fields.find((f) => f.segments.join(".") === segments.join("."));

  const body = (
    fields: readonly NamedField[],
    segments: readonly string[],
    indent: string,
  ): readonly string[] =>
    fields.flatMap((field): readonly string[] => {
      if (isUnaddressable(field.name)) {
        return [
          `${indent}{/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
        ];
      }
      const at = [...segments, field.name];
      switch (field.spec.kind) {
        case "object":
          return [
            `${indent}<fieldset${
              visual.columns > 1
                ? ui === "shadcn"
                  ? ` className="md:col-span-full"`
                  : ` style={{ gridColumn: "1 / -1" }}`
                : ""
            }>`,
            `${indent}  <legend>${jsxText(field.label)}</legend>`,
            ...body(field.spec.fields, at, `${indent}  `),
            `${indent}</fieldset>`,
          ];
        case "array":
          return [
            `${indent}{/* TODO: nested array ${commentText(q(at.join(".")))} — extract a row component with its own ${naming.hook("FieldArray")} */}`,
          ];
        default: {
          const planned = own(at);
          return planned === undefined
            ? []
            : [`${indent}<${planned.componentName} />`];
        }
      }
    });

  const fieldImports = collectLeaves(spec.fields, [section.key])
    .map(({ segments }) => own(segments))
    .flatMap((planned) => (planned === undefined ? [] : [planned]))
    .map(
      (planned) =>
        `import { ${planned.componentName} } from "../fields/${planned.componentName}";`,
    );

  return {
    path: `sections/${section.componentName}.tsx`,
    content: [
      HEADER,
      ...mergeImports(objectSectionImports(ui, visual)),
      `import { ${naming.hook("IsDirty")}, ${naming.hook("IsValid")} } from "../hooks";`,
      ...fieldImports,
      "",
      `export type ${propsType} = Readonly<{ heading?: string }>;`,
      "",
      "// The section hook is the path-scoped flags: dirty/valid for this",
      "// subtree only, as boolean-only subscriptions.",
      `export const ${hookName} = () => ({`,
      `  dirty: ${naming.hook("IsDirty")}(${q(section.key)}),`,
      `  valid: ${naming.hook("IsValid")}(${q(section.key)}),`,
      "});",
      "",
      `export const ${section.componentName} = ({`,
      `  heading = ${q(section.label)},`,
      `}: ${propsType}) => {`,
      `  const { dirty, valid } = ${hookName}();`,
      "  return (",
      ...objectShell(ui, visual, body(spec.fields, [section.key], "      ")),
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const arraySectionFile = (
  ui: ModuleUi,
  visual: VisualOptions,
  naming: Naming,
  section: SectionPlan,
): ModuleFile => {
  const spec = section.spec as Extract<FieldSpec, Readonly<{ kind: "array" }>>;
  const propsType = `${section.componentName}Props`;
  const hookName = `use${section.componentName}`;
  const rowName = `${section.componentName.replace(/Section$/, "")}Row`;
  const emptyName = `empty${section.componentName.replace(/Section$/, "")}Item`;
  const shell = arrayShell(ui, section.label);
  const itemLeaves =
    spec.item.kind === "object"
      ? spec.item.fields.filter(
          (field) => !isUnaddressable(field.name) && isScalar(field.spec),
        )
      : [];
  const itemTodos =
    spec.item.kind === "object"
      ? spec.item.fields.flatMap((field) => {
          if (isUnaddressable(field.name)) {
            return [
              `      {/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
            ];
          }
          return isScalar(field.spec)
            ? []
            : [
                `      {/* TODO: nested ${field.spec.kind} ${commentText(q(`${section.key}.$\{index}.${field.name}`))} — extract a row component with its own hook */}`,
              ];
        })
      : [];

  const used = new Set<string>(["index", "onRemove", "field", "rows"]);
  const rowVars = itemLeaves.map((field) => {
    const base = camelCase(field.name).length === 0 ? "value" : camelCase(field.name);
    const name = `${base}${identifierSuffix(base, used)}`;
    used.add(name);
    return { field, varName: name };
  });

  const rowSpecs =
    spec.item.kind === "object" ? itemLeaves.map((f) => f.spec) : [spec.item];
  const anyDateBinding = rowSpecs.some(isDateBinding);

  const dynamicId = (name: string | undefined): string =>
    name === undefined
      ? `{\`${templateEscape(section.key)}.\${index}\`}`
      : `{\`${templateEscape(section.key)}.\${index}.${templateEscape(name)}\`}`;

  const rowBindings =
    spec.item.kind === "object"
      ? rowVars.map(
          ({ field, varName }) =>
            `  const ${varName} = ${naming.hook("Field")}(\`${templateEscape(section.key)}.\${index}.${templateEscape(field.name)}\`)${isDateBinding(field.spec) ? DATE_CAST : ""};`,
        )
      : [
          `  const field = ${naming.hook("Field")}(\`${templateEscape(section.key)}.\${index}\`)${isDateBinding(spec.item) ? DATE_CAST : ""};`,
        ];

  const rowBody =
    spec.item.kind === "object"
      ? [
          ...rowVars.flatMap(({ field, varName }) =>
            leafControl(
              ui,
              field.spec,
              varName,
              jsxText(field.label),
              dynamicId(field.name),
              "      ",
            ),
          ),
          ...itemTodos,
        ]
      : leafControl(
          ui,
          spec.item,
          "field",
          jsxText(section.label),
          dynamicId(undefined),
          "      ",
        );

  const imports = mergeImports([
    ...rowSpecs.flatMap((s) => leafImports(ui, s)),
    ...shell.imports,
    // The outer wrapper is objectShell, so panel/collapsible chrome pulls
    // its own components in.
    ...objectSectionImports(ui, visual),
    ...(anyDateBinding
      ? [{ from: "formstand", names: ["type UseFieldReturn"] }]
      : []),
  ]);

  return {
    path: `sections/${section.componentName}.tsx`,
    content: [
      HEADER,
      ...imports,
      "import {",
      `  ${naming.hook("Field")},`,
      `  ${naming.hook("FieldArray")},`,
      `  ${naming.hook("IsDirty")},`,
      `  ${naming.hook("IsValid")},`,
      `} from "../hooks";`,
      `import type { ${naming.valuesType} } from "../types";`,
      "",
      `export type ${propsType} = Readonly<{ heading?: string }>;`,
      "",
      "// The array section's hook carries the field array alongside the",
      "// path-scoped flags; row fields bind template paths, so they stay",
      "// schema-typed.",
      `export const ${hookName} = () => ({`,
      `  rows: ${naming.hook("FieldArray")}(${q(section.key)}),`,
      `  dirty: ${naming.hook("IsDirty")}(${q(section.key)}),`,
      `  valid: ${naming.hook("IsValid")}(${q(section.key)}),`,
      "});",
      "",
      `const ${emptyName} = ${emitInitialValues(spec.item, 0)} as unknown as NonNullable<${naming.valuesType}[${q(section.key)}]>[number];`,
      "",
      `const ${rowName} = ({`,
      "  index,",
      "  onRemove,",
      "}: Readonly<{ index: number; onRemove: () => void }>) => {",
      ...rowBindings,
      "  return (",
      ...shell.rowOpen,
      ...rowBody,
      ...shell.rowClose("onRemove"),
      "  );",
      "};",
      "",
      `export const ${section.componentName} = ({`,
      `  heading = ${q(section.label)},`,
      `}: ${propsType}) => {`,
      `  const { rows, dirty, valid } = ${hookName}();`,
      "  return (",
      ...objectShell(ui, visual, [
        "      {rows.fields.map((row, index) => (",
        `        <${rowName}`,
        "          key={row.id}",
        "          index={index}",
        "          onRemove={() => rows.remove(index)}",
        "        />",
        "      ))}",
        ...shell.listError,
        ...shell.addButton(
          `() => rows.push(${emptyName})`,
          `Add ${section.label.toLowerCase()}`,
        ),
      ]),
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const formFile = (ui: ModuleUi, naming: Naming, plan: Plan): ModuleFile => {
  const shell = formShell(ui);
  return {
    path: `${naming.formName}.tsx`,
    content: [
      HEADER,
      ...mergeImports(shell.imports),
      `import { ${naming.formConst}, ${naming.hook("IsSubmitting")} } from "./hooks";`,
      ...plan.rootFields.map(
        (field) =>
          `import { ${field.componentName} } from "./fields/${field.componentName}";`,
      ),
      ...plan.sections.map(
        (section) =>
          `import { ${section.componentName} } from "./sections/${section.componentName}";`,
      ),
      "",
      "// The form body composes sections; sections compose fields; fields bind",
      "// paths. Nothing in this tree passes `form` — every hook comes pre-wired",
      "// from ./hooks.",
      `export const ${naming.formName} = () => {`,
      `  const submitting = ${naming.hook("IsSubmitting")}();`,
      "  return (",
      ...shell.open(naming),
      ...plan.rootTodos.map((todo) => `${shell.bodyIndent}${todo}`),
      ...plan.rootFields.map(
        (field) => `${shell.bodyIndent}<${field.componentName} />`,
      ),
      ...plan.sections.map(
        (section) => `${shell.bodyIndent}<${section.componentName} />`,
      ),
      ...shell.close,
      "  );",
      "};",
      "",
    ].join("\n"),
  };
};

const indexFile = (naming: Naming): ModuleFile => ({
  path: "index.ts",
  content: [
    HEADER,
    `export { ${naming.formName} } from "./${naming.formName}";`,
    "// The form and its pre-wired hooks are the module's public API.",
    `export * from "./hooks";`,
    "",
  ].join("\n"),
});

