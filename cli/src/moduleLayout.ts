const hooksFile = (
  naming: Naming,
  schemaImport: SchemaImport,
  root: ObjectSpec,
  plan: Plan,
): ModuleFile => {
  // Nested arrays (under object sections, or inside array rows) bind the
  // same bound hooks as top-level array sections, so look past the roots.
  const hasArrays = root.fields.some(
    (field) => !isUnaddressable(field.name) && specHasArray(field.spec),
  );
  // A union renders a discriminant + common fields (useField) and its
  // variant-only fields (useVariantField), so useField joins the API for any
  // union, but useVariantField only when a variant-only field exists (a union
  // of only common keys — e.g. a single-variant union — never emits one).
  const hasUnions = root.fields.some(
    (field) => !isUnaddressable(field.name) && specHasUnion(field.spec),
  );
  const hasVariantFields = root.fields.some(
    (field) => !isUnaddressable(field.name) && hasVariantFieldUsage(field.spec),
  );
  const hasFields = plan.fields.length > 0 || hasArrays || hasUnions;
  const hooks = [
    ...(hasFields ? [naming.hook("Field")] : []),
    ...(hasVariantFields ? [naming.hook("VariantField")] : []),
    ...(hasArrays ? [naming.hook("FieldArray")] : []),
    ...(plan.sections.length > 0
      ? [naming.hook("IsDirty"), naming.hook("IsValid")]
      : []),
    naming.hook("IsSubmitting"),
  ];
  return {
    path: "hooks.ts",
    content: [
      HEADER,
      `import { createForm, createFormHooks } from "formstand";`,
      `import { ${schemaImport.name} } from "./schema";`,
      `import type { ${naming.valuesType} } from "./types";`,
      "",
      ...(blankNeedsCast(root)
        ? [
            "// A form starts blank: required numbers/dates/enums begin",
            "// undefined, so these initial values intentionally do not satisfy",
            "// the schema yet; hence the cast. Validation reports the gaps on",
            "// submit.",
            `const initialValues = ${emitInitialValues(root, 0)} as unknown as ${naming.valuesType};`,
          ]
        : [
            "// A form starts blank, and every field here has a legal blank",
            '// state (strings "", booleans false, optional/nullable',
            "// undefined/null), so the draft typechecks as-is; no cast needed.",
            `const initialValues: ${naming.valuesType} = ${emitInitialValues(root, 0)};`,
          ]),
      "",
      "// One module-level form for the whole feature; every file below imports",
      "// these pre-wired hooks instead of receiving `form` via props or a",
      "// provider. A module singleton never unmounts — for a per-mount",
      "// lifecycle, use useForm + createFormContext instead.",
      `export const ${naming.formConst} = createForm(${schemaImport.name}, {`,
      "  initialValues,",
      `  mode: "onBlur",`,
      "});",
      "",
      "export const {",
      ...hooks.map((name) => `  ${name},`),
      `} = createFormHooks(${naming.formConst}, ${q(naming.factoryArg)});`,
      "",
    ].join("\n"),
  };
};

