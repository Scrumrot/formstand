type FieldPlan = Readonly<{
  segments: readonly string[];
  componentName: string; // CityField (identifierSuffix-deduped)
  spec: FieldSpec;
  label: string;
}>;

type SectionPlan = Readonly<{
  key: string;
  componentName: string; // AddressSection (deduped)
  label: string;
  spec:
    | ObjectSpec
    | Extract<FieldSpec, Readonly<{ kind: "array" }>>
    | Extract<FieldSpec, Readonly<{ kind: "union" }>>;
}>;

type Plan = Readonly<{
  fields: readonly FieldPlan[];
  sections: readonly SectionPlan[];
  rootFields: readonly FieldPlan[]; // top-level scalars, rendered in the body
  rootTodos: readonly string[]; // dot-keys etc. surfaced in the body
}>;

const isScalar = (spec: FieldSpec): boolean =>
  spec.kind !== "object" && spec.kind !== "array" && spec.kind !== "union";

// Whether the subtree holds a discriminated-union field at any addressable
// object depth — gates the bound Field/VariantField hook exports.
const specHasUnion = (spec: FieldSpec): boolean =>
  spec.kind === "union" ||
  (spec.kind === "object" &&
    spec.fields.some(
      (field) => !isUnaddressable(field.name) && specHasUnion(field.spec),
    )) ||
  (spec.kind === "array" && specHasUnion(spec.item));

// Whether the subtree holds an addressable array at any object depth. The
// walk does not need to cross array boundaries: an array inside an array is
// only reachable through the outer one, which already answers true.
const specHasArray = (spec: FieldSpec): boolean =>
  spec.kind === "array" ||
  (spec.kind === "object" &&
    spec.fields.some(
      (field) => !isUnaddressable(field.name) && specHasArray(field.spec),
    ));

// Scalar leaves under `fields` that are NOT inside an array (array rows bind
// dynamic paths inline in their section file instead).
const collectLeaves = (
  fields: readonly NamedField[],
  segments: readonly string[],
): readonly Readonly<{ segments: readonly string[]; field: NamedField }>[] =>
  fields.flatMap((field) => {
    if (isUnaddressable(field.name)) return [];
    const at = [...segments, field.name];
    switch (field.spec.kind) {
      case "object":
        return collectLeaves(field.spec.fields, at);
      case "array":
      // A union renders inline in its section file, not as a leaf field.
      case "union":
        return [];
      default:
        return [{ segments: at, field }];
    }
  });

const buildPlan = (root: ObjectSpec, naming: Naming): Plan => {
  // Pre-claim the bound hook's name shape: a field named after the module
  // prefix (field "contact" in ContactForm) would otherwise get
  // componentName ContactField, whose exported hook useContactField
  // collides with the identically named import from ../hooks — a duplicate
  // declaration plus a self-recursive call. Seeding pushes it to
  // ContactField2/useContactField2.
  const used = new Set<string>([`${naming.prefix}Field`]);
  const claim = (base: string): string => {
    const name = `${base}${identifierSuffix(base, used)}`;
    used.add(name);
    return name;
  };

  const planField = (
    segments: readonly string[],
    field: NamedField,
  ): FieldPlan => ({
    segments,
    componentName: claim(`${pascalCase(segments[segments.length - 1] ?? "")}Field`),
    spec: field.spec,
    label: field.label,
  });

  const rootFields = root.fields
    .filter((field) => !isUnaddressable(field.name) && isScalar(field.spec))
    .map((field) => planField([field.name], field));

  const sections = root.fields
    .filter(
      (field) =>
        !isUnaddressable(field.name) &&
        (field.spec.kind === "object" ||
          field.spec.kind === "array" ||
          field.spec.kind === "union"),
    )
    .map(
      (field): SectionPlan => ({
        key: field.name,
        componentName: claim(`${pascalCase(field.name)}Section`),
        label: field.label,
        spec: field.spec as SectionPlan["spec"],
      }),
    );

  const sectionLeafFields = sections.flatMap((section) =>
    section.spec.kind === "object"
      ? collectLeaves(section.spec.fields, [section.key]).map(
          ({ segments, field }) => planField(segments, field),
        )
      : [],
  );

  const rootTodos = root.fields
    .filter((field) => isUnaddressable(field.name))
    .map(
      (field) =>
        `{/* TODO: field ${commentText(q(field.name))} skipped — "." in a key is not path-addressable (see formstand docs) */}`,
    );

  return {
    fields: [...rootFields, ...sectionLeafFields],
    sections,
    rootFields,
    rootTodos,
  };
};

